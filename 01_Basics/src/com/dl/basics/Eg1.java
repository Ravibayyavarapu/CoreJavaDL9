package com.dl.basics;

public class Eg1 {

	public static void main(String[] args) {

		// Primitive Data types:
		byte b1 = 10;
		int i1 = 20;
		short s1 = 30;
		long l1 = 40l;
		System.out.println(b1 + i1 + s1 + l1);

		float f1 = 10.5f;
		double d1 = 20.5d;
		System.out.println(f1 + d1);

		char ch = 'A';
		System.out.println(ch);

		boolean b2 = true;
		System.out.println(b2);

		// Non Primitive Data types
		// String
		// Arrays
		// Collections

		// Wrapper Types
		// Byte
		// Short
		// Integer
		// Long
		// Float
		// Double
		// Character
		// Boolean

		// Wrapper types:
		Byte b11 = 10;
		Integer i11 = 20;
		Short s11 = 30;
		Long l11 = 40l;
		System.out.println(b11 + i11 + s11 + l11);

		Float f11 = 10.5f;
		Double d11 = 20.5d;
		System.out.println(f11 + d11);

		Character chh = 'A';
		System.out.println(chh);

		Boolean b22 = true;
		System.out.println(b22);
		
		//Java is 99% Object O PL because of Primitive Data types 
		//Support of Wrapper classes
		
		

	}

}
