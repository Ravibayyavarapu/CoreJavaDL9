package com.dl.one;

public class Eg3 {

	public static void main(String[] args) {
	
		//Unary Operators ++ --
		int a = 10;
		System.out.println(a++); //10 //After:Increase Later
		System.out.println(a); // 11
		
		int b = 20;
		System.out.println(++b); // 21 //Before: Increase First
		System.out.println(b);
		
		System.out.println(b++); //21
		System.out.println(b); //22

	}

}
